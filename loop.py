class Stack(object):
    def __init__(self,size=256):
        self.s = [None] * size
        self.top = 0
    def __repr__(self):
        return "{} : ".format(self.top) + repr(self.s[0:10]) 
    @property
    def IsEmpty(self):
        return self.top == 0
    def push(self,val):
        self.s[self.top] = val
        self.top += 1
        return self.top
    def pop(self):
        self.top -= 1
        val = self.s[self.top]
        self.s[self.top] = None
        return val
