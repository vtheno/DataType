    def unop(self,sym):
        def inlineUnop(t,tl):
            return MkComb(t,Node(sym,tl))
        return inlineUnop
    def Parser_loop (self,m,acc):
        self.s.push( acc )
        while not self.s.IsEmpty:
            acc = self.s.pop ()
            if self.inp == [ ]:
                continue
            x,xs = unpack(self.inp)
            n,parsefn = self.symtab.get(x,self.symtab[Any] )
            if m >= n:
                break
            else:
                if parsefn == Sym.ParserAtom:
                    x,self.inp = unpack(self.inp)
                    acc = MkComb(acc,Atom(x))
                    self.s.push( acc )
                    continue
                elif parsefn == Sym.Terminator: # 
                    raise Exception ("TerminatorErr")
                elif isinstance(parsefn,tuple):
                    drop,self.inp = unpack(self.inp)
                    close = parsefn[0]
                    self.recursion_depth += 1
                    with T(self):
                        acc1,inp1 = self.Parser_loop(0,Nil)
                        x,self.inp = unpack( self.inp )
                        if close == x :
                            acc = MkComb(acc,acc1)
                            self.s.push( acc ) 
                            continue
                        else:
                            raise Exception("MissingClosingBracket")                    
                    self.recursion_depth -= 1
                elif isinstance(parsefn,list):
                    head,n,funclist = parsefn 
                    fs = funclist
                    if head[0] == "binop":
                        mktree = self.binop(head[1])
                    elif head[0] == "unop":
                        mktree = self.unop(head[1])
                    else:
                        raise Exception("MkTreeErr") # seq_m = n ,seq_n = m
                    seq_m,seq_n = n , m
                    drop,self.inp = unpack(self.inp)
                    seq_acc = [ ]
                    while len(fs) > 1:
                        f,fs = unpack(fs)
                        if isinstance(f,list):
                            skip = f[0]
                            self.recursion_depth += 1
                            with T(self):
                                acc1,rest = self.Parser_loop(0,Nil)
                                s1,rest1 = unpack(rest)
                                if skip == s1:
                                    t,self.inp = acc1,rest1
                                else:
                                    raise Exception("StripErr")
                            self.recursion_depth -= 1
                        else:
                            self.recursion_depth += 1
                            with T(self):
                                t,self.inp = self.Parser_loop(0,Nil)
                            self.recursion_depth -= 1
                        seq_acc += [t]
                    f = fs[0] 
                    # t,self.inp = f(seq_m,Nil)
                    if isinstance(f,list):
                        skip = f[0]
                        self.recursion_depth += 1
                        with T(self):
                            acc1,rest = self.Parser_loop(seq_m,Nil)
                            s1,rest1 = unpack(rest)
                            if skip == s1:
                                t,self.inp = acc1,rest1
                            else:
                                raise Exception("StripErr")
                        self.recursion_depth -= 1
                    else:
                        self.recursion_depth += 1
                        with T(self):
                            t,self.inp = self.Parser_loop(m,Nil)
                        self.recursion_depth -= 1
                    seq_acc += [t]
                    m = seq_n
                    acc = mktree(acc,seq_acc)
                    self.s.push( acc )
                    continue
                else:
                    raise Exception ("ParserErr")
        return (acc,self.inp)
